# arithmetic operators - performs simple
x = 60
y = 7

print(x + y) # Addtion
print(x - y) # Subtraction
print(x * y) # Multiplication
print(x / y) #Division
print(x % y) # Modulus - it returns the remainder

# Comparison Operator - compare values
a = 34
b = 23
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b) # equal to
print(a != b) # not equal to

